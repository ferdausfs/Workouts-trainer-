  static List<Exercise> byMuscle(MuscleGroup group) =>
      all.where((e) =>
          e.primaryMuscles.contains(group) ||
          e.secondaryMuscles.contains(group)).toList();

  static Exercise? byId(String id) =>
      all.where((e) => e.id == id).cast<Exercise?>().firstWhere(
          (e) => true,
          orElse: () => null);
